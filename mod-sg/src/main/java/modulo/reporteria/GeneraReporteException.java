package modulo.reporteria;

public class GeneraReporteException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 161848263L;

	public GeneraReporteException() {
	}

	public GeneraReporteException(String message) {
		super(message);

	}

	public GeneraReporteException(Throwable cause) {
		super(cause);

	}

	public GeneraReporteException(String message, Throwable cause) {
		super(message, cause);

	}

	public GeneraReporteException(String message, Throwable cause,
			boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

}
